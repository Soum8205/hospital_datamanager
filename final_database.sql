CREATE DATABASE IF NOT EXISTS HospitalDB;
USE HospitalDB;


DROP TABLE IF EXISTS Bill;
DROP TABLE IF EXISTS Treatment;
DROP TABLE IF EXISTS Appointment;
DROP TABLE IF EXISTS Patient;
DROP TABLE IF EXISTS Doctor;
DROP TABLE IF EXISTS Department;

CREATE TABLE Department (
    department_id   INT PRIMARY KEY AUTO_INCREMENT,
    department_name VARCHAR(50) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE Doctor (
    doctor_id      INT PRIMARY KEY AUTO_INCREMENT,
    name           VARCHAR(50),
    specialization VARCHAR(50),
    phone          VARCHAR(15),
    department_id  INT,
    FOREIGN KEY (department_id) REFERENCES Department(department_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE Patient (
    patient_id INT PRIMARY KEY AUTO_INCREMENT,
    name       VARCHAR(50),
    age        INT,
    gender     VARCHAR(10),
    phone      VARCHAR(15),
    address    VARCHAR(100)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE Appointment (
    appointment_id   INT PRIMARY KEY AUTO_INCREMENT,
    patient_id       INT,
    doctor_id        INT,
    appointment_date DATE,
    status           VARCHAR(20),
    FOREIGN KEY (patient_id) REFERENCES Patient(patient_id),
    FOREIGN KEY (doctor_id)  REFERENCES Doctor(doctor_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE Treatment (
    treatment_id      INT PRIMARY KEY AUTO_INCREMENT,
    appointment_id    INT,
    diagnosis         VARCHAR(100),
    treatment_details VARCHAR(200),
    cost              DECIMAL(10,2),
    FOREIGN KEY (appointment_id) REFERENCES Appointment(appointment_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE Bill (
    bill_id        INT PRIMARY KEY AUTO_INCREMENT,
    treatment_id   INT,
    bill_date      DATE,
    total_amount   DECIMAL(10,2),
    payment_status VARCHAR(20),
    FOREIGN KEY (treatment_id) REFERENCES Treatment(treatment_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;


DROP TRIGGER IF EXISTS generate_bill;

DELIMITER //
CREATE TRIGGER generate_bill
AFTER INSERT ON Treatment
FOR EACH ROW
BEGIN
    INSERT INTO Bill (treatment_id, bill_date, total_amount, payment_status)
    VALUES (NEW.treatment_id, CURDATE(), NEW.cost, 'Pending');
END //
DELIMITER ;

DROP PROCEDURE IF EXISTS AddPatient;

DELIMITER //
CREATE PROCEDURE AddPatient(
    IN pname    VARCHAR(50),
    IN page     INT,
    IN pgender  VARCHAR(10),
    IN pphone   VARCHAR(15),
    IN paddress VARCHAR(100)
)
BEGIN
    INSERT INTO Patient(name, age, gender, phone, address)
    VALUES (pname, page, pgender, pphone, paddress);
END //
DELIMITER ;

DROP VIEW IF EXISTS DoctorSchedule;

CREATE VIEW DoctorSchedule AS
SELECT
    d.name           AS Doctor_Name,
    a.appointment_date,
    a.status
FROM Doctor d
JOIN Appointment a ON d.doctor_id = a.doctor_id;

